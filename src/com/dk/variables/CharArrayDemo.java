package com.dk.variables;

public class CharArrayDemo {
	public static void main(String[] args) {
		
		char[] ch = new char[5];
		ch[0]='a';
		ch[1]='i';
		ch[2]='o';
		ch[3]='u';
		ch[4]='e';
		System.out.println("The first vowel stored is="+ch[0]);	
		System.out.println("The second vowel stored is="+ch[1]);
		System.out.println("The third vowel stored is="+ch[2]);
		System.out.println("The fourth vowel stored is="+ch[3]);
		System.out.println("The fifth vowel stored is="+ch[4]);
		
	}

}
